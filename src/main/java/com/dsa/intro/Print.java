package com.dsa.intro;

public class Print {
    public static void main(String[] args) {
        System.out.println("Hello, DSA Practice!"); // Hello, DSA Practice!
        System.out.println("Hey there"); // Hey there
        System.out.println(10); // 10
        System.out.println("90 * 50"); // 90 * 50
        System.out.println(90 * 50); // 4500
        System.out.println(10 + 20 + 30); // 60
        System.out.println("10" + 20 + 50); // 102050
        System.out.println(20 + 50 + "10"); // 7010
    }
}